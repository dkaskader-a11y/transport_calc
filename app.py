with st.expander("Описание сервиса и логики расчёта", expanded=False):
    st.markdown("""
### Что делает сервис
Сервис рассчитывает, сколько транспорта потребуется для перевозки груза на основе Excel-файла с габаритами, количеством мест, признаком штабелирования и, при наличии, весом.

### Как работает расчёт
Алгоритм работает в несколько этапов:

1. **Читает входной файл**
   - приводит размеры и количество к числам
   - если вес не указан, считает его как `0` и помечает как неизвестный
   - если количество больше 1, размножает позицию на отдельные грузовые места

2. **Проверяет габариты**
   - груз должен проходить по длине/ширине пола
   - поворот по полу разрешён
   - **ограничение по высоте учитывается**
   - если место не проходит ни в один выбранный тип транспорта, оно попадает в негабарит

3. **Укладывает груз**
   - сначала размещает груз **по полу** методом рядов (`shelf packing`)
   - затем пытается использовать **штабелирование**
   - если груз **не штабелируется**:
     - его нельзя ставить сверху
     - на него нельзя ставить другие места
   - если груз **штабелируется**:
     - его можно ставить сверху
     - на него можно ставить другие места
   - при штабелировании учитываются:
     - размеры верхнего места относительно базы
     - общая высота стопки
     - допустимая нагрузка сверху

4. **Подбирает смешанный парк**
   - сначала пытается закрыть остаток самым маленьким подходящим транспортом
   - если это невозможно, берёт машину, которая увозит максимум мест за один рейс
   - затем повторяет расчёт для остатка

### Как учитывается вес
- если у всех грузов вес известен, алгоритм учитывает грузоподъёмность
- если часть весов отсутствует, подбор транспорта идёт **по геометрии**, чтобы не занизить количество машин

### Что означает итог
В результате сервис показывает:
- состав парка по типам транспорта
- статистику по каждой машине
- негабарит
- неразмещённые места
- диагностические показатели по грузу
""")

